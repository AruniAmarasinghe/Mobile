package com.edebtor.oclimb.edebtor.Model;

public class two_item {

     String id;
     String value;

    public two_item(String id,String value){

        this.id=id;
        this.value=value;

    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
